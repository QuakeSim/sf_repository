package cgl.quakesim.automateddisloc;

public class OutputURLs {
	
	private String insarkmlURL = null;
	private String displacementkmlURL = null;
	private String dislocoutputURL = null;
	
	
	public String getInsarkmlURL() {
		return insarkmlURL;
	}
	public void setInsarkmlURL(String insarkmlURL) {
		this.insarkmlURL = insarkmlURL;
	}
	public String getDisplacementkmlURL() {
		return displacementkmlURL;
	}
	public void setDisplacementkmlURL(String displacementkmlURL) {
		this.displacementkmlURL = displacementkmlURL;
	}
	public String getDislocoutputURL() {
		return dislocoutputURL;
	}
	public void setDislocoutputURL(String dislocoutputURL) {
		this.dislocoutputURL = dislocoutputURL;
	}
}
